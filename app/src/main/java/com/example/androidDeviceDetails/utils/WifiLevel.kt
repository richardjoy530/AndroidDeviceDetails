package com.example.androidDeviceDetails.utils

/**
 * An enum class used to represent different wifi levels.
 *
 * 0 represent NONE.
 *
 * 1 represent POOR.
 *
 * 2 represent MODERATE.
 *
 * 3 represent GOOD.
 *
 * 4 represent GREAT.
 */
enum class WifiLevel {
    NONE,
    POOR,
    MODERATE,
    GOOD,
    GREAT
}